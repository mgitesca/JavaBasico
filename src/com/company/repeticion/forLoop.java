package com.company.repeticion;

public class forLoop {
    public static void main(String[] args) {
        // for (<inicio>; <fin>, <paso>)
        for (int i=0; i<=10; i++){
            System.out.println(i + " Hola mundo");
        }
        // i ++ incrementa en 1 el valor de i
        // i +=1 es lo mismo

    }
}
